    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Placa pl = new Placa();
        Modelo modelo = new Modelo();
        Date date = fecha.getDate();
        String fechaSeleccionada = pl.formatearFecha(date);
        StringBuilder salida = new StringBuilder();
        salida.append("Fecha Seleccionada: ").append(fechaSeleccionada).append("   ");

        Date horas = (Date) hora.getValue();
        DateFormat hourFormat = new SimpleDateFormat("HH:mm");
        String horaSeleccionada = hourFormat.format(horas);
        salida.append("Hora Seleccionada: ").append(horaSeleccionada).append("\n").append("\n");
        List<Integer> horasMinutos = pl.obtenerHoraMinuto(horaSeleccionada);

        Object[] dia = modelo.recorrerArreglo(pl.obtenerDia(fechaSeleccionada));
        if (dia != null) {
            for (int j = 0; j < dia.length; j++) {
                if (j == 0) {
                    salida.append("Ultimos Digitos: ");
                }

                if (j == 2) {
                    salida.append("   MaÃ±ana: ");
                }
                if (j == 4) {
                    salida.append("   Tarde: ");
                }
                salida.append(dia[j].toString()).append(" ");
            }
            salida.append("\n");
        } else {
            salida.append("Fines de Semana No se aplica Pico y Placa" + "\n");
        }
        salida.append("\n");
        String lineas;
        try {
            BufferedReader bufferAux = archivo.getBuffer();
            if (archivo.getBuffer() == null) {
                salida.append("No ha seleccionado ningun fichero" + "\n");
            } else {
                while ((lineas = archivo.getBuffer().readLine()) != null) {
                    if (pl.validarPlaca(lineas)) {
                        salida.append("Placa con formato CORRECTO ").append(lineas).append("\n");

                        salida.append(modelo.validarDiaPlaca(pl.obtenerDia(fechaSeleccionada), pl.obtenerUltimoDigito(lineas), horasMinutos)).append("\n");
                    } else {
                        salida.append("Placa con formato INCORRECTO:  ").append(lineas).append("\n\n");
                    }
                }
            }
            archivo.setBuffer(bufferAux);
            nota.setText("");
            salidaTxt.setText(salida.toString());
        } catch (IOException ex) {
            Logger.getLogger(Vista.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton1ActionPerformed
